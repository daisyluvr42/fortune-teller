# 十二时辰对应的小时范围
SHICHEN_HOURS = {
    "子时 (23:00-01:00)": (23, 0),  # 跨日
    "丑时 (01:00-03:00)": (1, 2),
    "寅时 (03:00-05:00)": (3, 4),
    "卯时 (05:00-07:00)": (5, 6),
    "辰时 (07:00-09:00)": (7, 8),
    "巳时 (09:00-11:00)": (9, 10),
    "午时 (11:00-13:00)": (11, 12),
    "未时 (13:00-15:00)": (13, 14),
    "申时 (15:00-17:00)": (15, 16),
    "酉时 (17:00-19:00)": (17, 18),
    "戌时 (19:00-21:00)": (19, 20),
    "亥时 (21:00-23:00)": (21, 22),
}

def get_shichen_mid_hour(shichen: str) -> int:
    """获取时辰的中间小时值"""
    hours = SHICHEN_HOURS.get(shichen, (12, 12))
    if shichen == "子时 (23:00-01:00)":
        return 0  # 返回0点作为子时中点
    return hours[0] + 1  # 返回时辰中间的小时
